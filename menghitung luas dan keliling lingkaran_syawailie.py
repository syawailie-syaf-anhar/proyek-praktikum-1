#variabel rumus
luas = jari_jari * jari_jari * phi #rumus luas linkaran
keliling = 2 * phi * jari_jari  #rumus keliling lingkaran

#menampilakan hasil luas lingkaran
print("luas lingkaran: ", luas)
#menampilkan hasil keliling lingkaran
print ("keliling lingkaran: ", keliling)
